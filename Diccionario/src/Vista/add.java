    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Pal = jTextField1.getText();
        String Definicion = jTextArea2.getText();
        String Ejemplos = jTextArea1.getText();
        int validar = 0;
        if (Pal.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan datos en Palabra");
            validar = 1;
        }
        if (Definicion.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan datos en Definicion");
            validar = 1;
        }
        if (Ejemplos.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan datos en Ejemplos");
            validar = 1;
        }

        if (validar == 0) {
            Palabra addPalabra = new Palabra();
            try {
                addPalabra.insertarPalabra(Pal, Definicion, Ejemplos);
            } catch (SQLException ex) {
                System.out.println(ex.toString());
            }
            jTextField1.setText("");
            jTextArea1.setText("");
            jTextArea2.setText("");
            JOptionPane.showMessageDialog(null, "Datos agregados exitosamente ");
        }

    }//GEN-LAST:event_jButton1ActionPerformed


